package lab7;

public class Employee {
	private String name;
    private int employeeNumber;
    
    public Employee(String name, int employeeNumber) {
        this.name = name;
        this.employeeNumber = employeeNumber;
    }
  
    public String getName() {
        return name;
    }
   
    public int getEmployeeNumber() {
        return employeeNumber;
    }
}







